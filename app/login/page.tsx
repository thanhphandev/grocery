function LoginContent() {
    const router = useRouter();
    const searchParams = useSearchParams();
    const { data: session, isPending } = useSession();
    const [isSigningIn, setIsSigningIn] = useState(false);

    // If already authenticated, redirect
    if (session && !isPending) {
        const callbackUrl = searchParams.get("callbackUrl") || "/";
        router.replace(callbackUrl);
        return null;
    }

    const handleGoogleSignIn = useCallback(async () => {
        setIsSigningIn(true);
        try {
            await signIn.social({
                provider: "google",
                callbackURL: searchParams.get("callbackUrl") || "/",
            });
        } catch {
            setIsSigningIn(false);
        }
    }, [searchParams]);

    return (
        <div className="min-h-screen flex flex-col items-center justify-center p-6 relative overflow-hidden bg-gradient-to-br from-background via-background to-primary/5">
            {/* Background decorative elements */}
            <div className="absolute inset-0 overflow-hidden pointer-events-none">
                <div className="absolute -top-32 -right-32 w-80 h-80 rounded-full bg-primary/5 blur-3xl" />
                <div className="absolute -bottom-40 -left-40 w-96 h-96 rounded-full bg-primary/8 blur-3xl" />
                <div className="absolute top-1/4 left-1/2 -translate-x-1/2 w-64 h-64 rounded-full bg-primary/3 blur-3xl" />
            </div>

            {/* Main card */}
            <motion.div
                initial={{ opacity: 0, y: 24, scale: 0.96 }}
                animate={{ opacity: 1, y: 0, scale: 1 }}
                transition={{ duration: 0.5, ease: [0.25, 0.46, 0.45, 0.94] }}
                className="relative w-full max-w-sm"
            >
                {/* Glassmorphism card */}
                <div className="rounded-3xl p-8 bg-card/80 backdrop-blur-xl border border-border/50 shadow-2xl shadow-primary/5">
                    {/* Logo */}
                    <motion.div
                        initial={{ scale: 0 }}
                        animate={{ scale: 1 }}
                        transition={{
                            delay: 0.15,
                            type: "spring",
                            stiffness: 260,
                            damping: 20,
                        }}
                        className="flex justify-center mb-6"
                    >
                        <div className="w-20 h-20 rounded-2xl bg-gradient-to-br from-primary to-primary/80 flex items-center justify-center shadow-lg shadow-primary/25">
                            <Zap className="w-10 h-10 text-primary-foreground" />
                        </div>
                    </motion.div>

                    {/* Title */}
                    <motion.div
                        initial={{ opacity: 0, y: 8 }}
                        animate={{ opacity: 1, y: 0 }}
                        transition={{ delay: 0.25 }}
                        className="text-center mb-8"
                    >
                        <h1 className="text-2xl font-extrabold text-foreground tracking-tight mb-1.5">
                            SPEED-PRICE
                        </h1>
                        <p className="text-sm text-muted-foreground font-medium">
                            Tra cứu giá sản phẩm siêu tốc
                        </p>
                    </motion.div>

                    {/* Google Sign In Button */}
                    <motion.button
                        type="button"
                        initial={{ opacity: 0, y: 8 }}
                        animate={{ opacity: 1, y: 0 }}
                        transition={{ delay: 0.35 }}
                        whileTap={{ scale: 0.97 }}
                        onClick={handleGoogleSignIn}
                        disabled={isSigningIn || isPending}
                        className="w-full flex items-center justify-center gap-3 px-6 py-4 rounded-2xl bg-foreground text-background font-semibold text-[15px] shadow-lg hover:opacity-90 transition-all disabled:opacity-50 disabled:cursor-not-allowed active:shadow-md"
                    >
                        {isSigningIn || isPending ? (
                            <Loader2 className="w-5 h-5 animate-spin" />
                        ) : (
                            <GoogleIcon className="w-5 h-5" />
                        )}
                        <span>
                            {isSigningIn
                                ? "Đang đăng nhập..."
                                : "Đăng nhập bằng Google"}
                        </span>
                    </motion.button>

                    {/* Security note */}
                    <motion.div
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        transition={{ delay: 0.5 }}
                        className="flex items-center justify-center gap-1.5 mt-6"
                    >
                        <ShieldCheck className="w-3.5 h-3.5 text-muted-foreground/60" />
                        <span className="text-[11px] text-muted-foreground/60 font-medium">
                            Bảo mật bởi Google OAuth 2.0
                        </span>
                    </motion.div>
                </div>
            </motion.div>

            {/* Footer */}
            <motion.p
                initial={{ opacity: 0 }}
                animate={{ opacity: 1 }}
                transition={{ delay: 0.6 }}
                className="mt-8 text-[11px] text-muted-foreground/40 font-medium text-center"
            >
                © {new Date().getFullYear()} SPEED-PRICE • Dành cho nhân viên bán
                hàng
            </motion.p>
        </div>
    );
}

export default function LoginPage() {
    return (
        <Suspense
            fallback={
                <div className="min-h-screen flex items-center justify-center bg-background">
                    <Loader2 className="w-8 h-8 animate-spin text-primary" />
                </div>
            }
        >
            <LoginContent />
        </Suspense>
    );
}
